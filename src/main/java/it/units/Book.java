package it.units;

public class Book {
    private String id;
    private String titolo;
    private String autore;
    private String anno;

    public String getId(){
        return id;
    }

    public String getAnno() {
        return anno;
    }

    public String getAutore() {
        return autore;
    }


    public String getTitolo() {
        return titolo;
    }

}
